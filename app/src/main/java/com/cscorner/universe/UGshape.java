package com.cscorner.universe;

public class UGshape {
    int ugimg;
    String ugname;

    public UGshape(int ugimg, String ugname) {
        this.ugimg = ugimg;
        this.ugname = ugname;
    }

    public int getUgimg() {
        return ugimg;
    }

    public void setUgimg(int ugimg) {
        this.ugimg = ugimg;
    }

    public String getUgname() {
        return ugname;
    }

    public void setUgname(String ugname) {
        this.ugname = ugname;
    }
}
