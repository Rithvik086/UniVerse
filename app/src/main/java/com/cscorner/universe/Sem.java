package com.cscorner.universe;

public class Sem {
    int ugsemimg;
    String semno;

    public Sem(int ugsemimg, String semno) {
        this.ugsemimg = ugsemimg;
        this.semno = semno;
    }

    public int getUgsemimg() {
        return ugsemimg;
    }

    public void setUgsemimg(int ugsemimg) {
        this.ugsemimg = ugsemimg;
    }

    public String getSemno() {
        return semno;
    }

    public void setSemno(String semno) {
        this.semno = semno;
    }
}
